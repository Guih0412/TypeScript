// Soma
class Soma {
    public calcular(numero1: number, numero2: number) {
        return numero1 + numero2
    }
}

// Subtração
class Subtracao {
    public calcular(numero1: number, numero2: number) {
        return numero1 - numero2
    }
}

// Multiplicação
class Multiplicacao {
    public calcular(numero1: number, numero2: number) {
        return numero1 * numero2
    }
}

// Divisão
class Divisao {
    public calcular(numero1: number, numero2: number) {
        return numero1 / numero2
    }
}

export { Soma, Subtracao, Multiplicacao, Divisao }
